package edu.tomm;

class StaticMethods {
    /*
    Qui si prova come in metodi statici presenti in altre classi sono chiamabili come se fossero
    nella classe che li invoca, si nota che essi devono essere per forza STATICI
     */

    static double divisione(double op1, double op2){
        return op1/op2;
    }
}
